/* ============================================================
   Section C: Scenario-Based Questions
   ============================================================ */

/*
1) How would you handle an API call that takes time to respond?
- Use async/await
- Show loading message
- Use try/catch for error handling
*/

/*
2) Why should long tasks not block the main thread?
- JS runs on single main thread
- If blocked:
  - UI freezes
  - Page becomes unresponsive
*/

/*
3) Which async method would you choose for a large application and why?
- async/await
- Because code is clean, readable, and easy to maintain with try/catch
*/

/*
4) How do promises improve readability?
- Avoid nested callbacks
- Allows chaining using .then()
- Easier error handling using .catch()
*/

/*
5) How does async/await simplify debugging?
- Looks like normal synchronous code
- Easier to track errors using try/catch
- Easier step-by-step debugging
*/